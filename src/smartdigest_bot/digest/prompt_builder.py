from __future__ import annotations

from smartdigest_bot.models import DigestCandidate
from smartdigest_bot.utils.text import truncate


def build_digest_prompt(posts: list[DigestCandidate]) -> str:
    post_count = len(posts)
    lines = [
        "Сделай краткий дайджест для Telegram строго на русском языке.",
        "",
        "Жесткие правила:",
        "- используй ТОЛЬКО информацию из постов ниже;",
        "- НЕ добавляй внешние факты, новости, рыночные данные, даты, проекты или выводы, которых нет в исходных постах;",
        "- если данных недостаточно, просто кратко перескажи то, что есть, без догадок;",
        "- не используй английские заголовки вроде 'Key takeaway', 'Market Pressure', 'Active Opportunities' и т.п.;",
        "- не используй таблицы;",
        "- НЕ объединяй несколько постов в одну общую тему;",
        f"- в ответе должно быть РОВНО {post_count} пунктов: по одному пункту на каждый исходный пост;",
        "- НИ ОДИН пост не должен быть пропущен;",
        "- каждый пункт должен сжимать именно информацию одного поста: оставить только важные действия, дедлайны, цифры, условия, статусы, ссылки и ключевой смысл без воды;",
        "- если в посте есть действие для пользователя, начни пункт с 'Действие:';",
        "- если в посте нет действия, просто кратко дай суть и важные факты;",
        "- в конце каждого пункта обязательно оставь ссылку на оригинальный пост;",
        "- не придумывай общие категории и не группируй посты по темам;",
        "- формат ответа: компактный русский список, где каждый пункт начинается с номера исходного поста;",
        "",
        "Исходные посты:",
    ]
    for index, post in enumerate(posts, start=1):
        lines.append(
            f"{index}. @{post.channel_username} | {post.external_post_url}\n"
            f"Текст поста: {truncate(post.content_text, 1200)}"
        )
    return "\n".join(lines)
